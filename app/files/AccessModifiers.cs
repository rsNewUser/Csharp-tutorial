/*
there are basic four access modifiers

public : the code with public modifiers can be accessed
anywhere outside as well as inside the class 

private : the code in private modifiers can be accessed 
only inside the class and cannot outside the class

protected : the code with protected modifiers can be accessed
only inside the class and class inherited from the same class

internal : the code with internal modifiers can be accessed
inside the same code block not outside  the code block even 
not in the same class 


*/